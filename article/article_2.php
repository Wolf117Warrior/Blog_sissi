                  <h3>La Reine de Hongrie</h3>

                  <img src="https://upload.wikimedia.org/wikipedia/commons/f/fd/Erzsebet_kiralyne_photo_1867.jpg?uselang=fr" alt="Sissi de Wittelsbach" style="width:290px;height:450px;">

                  <p>Le 8 juin 1867, <strong>Élisabeth</strong>, passionnée par la Hongrie, sa langue, et son peuple, est couronnée <strong>reine de Hongrie</strong> aux côtés de son mari (c'est la naissance de
                    la double monarchie austro-hongroise). Le compositeur Liszt, présent à la cérémonie, dira d'elle émerveillé : </p>
                    <p><em>« Elle n'avait jamais été aussi belle... elle apparaissait comme une vision céleste dans le déroulement d'un faste barbare ».</em></p>
                  <p>Élisabeth traduit spontanément son prénom en hongrois <strong>Erzsébet</strong>, une souveraine admirée et acclamée par le peuple magyar ravi par la mesure de clémence consécutive
                    au couronnement, l'amnistie de tous les délits politiques commis en Hongrie depuis 1848. L'État hongrois lui offre à cette occasion le <strong>château de Gödöllő</strong> situé à une
                    trentaine de kilomètres de <strong>Budapest</strong>. C'est le seul véritable endroit où elle se sent chez elle. Elle s'y rendra très souvent.</p>
                  <p>Le rôle politique d'<strong>Élisabeth</strong> dans l'élaboration du Compromis austro-hongrois, sans avoir été déterminant, est incontestable. Au moins dans l'influence qu'elle eut
                    auprès de François-Joseph à surmonter sa répugnance vis-à-vis des <strong>Magyars</strong> et celle de ces derniers à l'encontre de leur roi. La répression de la révolution hongroise
                    de 1848/1849 avait laissé des traces d'amertume d'autant plus profondes dans les élites et dans le peuple hongrois qu'il avait fallu que François-Joseph, débutant,
                    fasse appel aux troupes russes pour rétablir l'ordre.</p>
                  <p>La joie éprouvée lors du compromis avec la Hongrie raviva pour une courte période sa relation avec <strong>François-Joseph</strong> et <strong>Sissi</strong> revint sur sa décision de ne plus avoir
                    d'enfant. Un an après le couronnement, c'est à Budapest qu'elle choisit de donner le jour à son quatrième enfant, une fille, prénommé originalement Marie-Valérie
                    (la région de Budapest est la Valeria). La reine de Hongrie aurait préféré un fils qui serait devenu plus tard roi de Hongrie consommant la séparation des deux
                    monarchies. Cependant cette couronne de Hongrie et la naissance de cette enfant, pour laquelle elle éprouvera toute sa vie un amour exclusif et oppressant, marqueront
                    un tournant dans la vie d'<strong>Elisabeth</strong>. Elle s'est enfin imposée.</p>

