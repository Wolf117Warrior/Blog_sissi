                  <h3>L'Impératrice d'Autriche</h3>

                  <img src="https://upload.wikimedia.org/wikipedia/commons/e/e5/Winterhalter_Elisabeth_in_true_collours.jpg?uselang=fr" alt="Sissi de Wittelsbach" style="width:290px;height:450px;">

                  <p>Le jeune empereur <strong>François-Joseph d'Autriche</strong>, vivant dans une étroite complicité, voire sous l'emprise de sa mère l'<strong>archiduchesse Sophie</strong>, avait choisi la sœur
                    aînée de Sissi, la <strong>duchesse Hélène</strong> en Bavière, âgée de 19 ans. Les fiançailles devaient être célébrées le 18 août 1853, dans la résidence impériale d’été de Bad Ischl,
                    à l'occasion des fêtes données pour le vingt-troisième anniversaire du souverain autrichien.</p>
                  <p>Mais c’est de Sissi que le jeune souverain va s'éprendre. À la surprise de sa mère, l’autoritaire archiduchesse Sophie, il annonce le 19 août son intention d'épouser
                    la jeune Sissi, à peine âgée de 15 ans et 8 mois.</p>
                  <p>Le mariage est célébré le <strong>24 avril 1854</strong> à <strong>Vienne, en Autriche</strong>. Contrairement à la tradition, la nuit de noces n'est pas publique. Elle se passe d'ailleurs assez mal.
                    Le jeune empereur, habitué à des dames peu farouches et audacieuses, ne sait pas apprivoiser la jeune fille inexpérimentée qu'il a épousée. La cour est également surprise
                    par cette jeune impératrice qui se laisse aller à ses états d'âmes. Il est vrai qu'à l'époque le rôle d'une souveraine se cantonne à être disponible et soumise à son mari.
                    De plus, la jeune impératrice, habituée aux manières simples de son entourage provincial, supporte mal la pesante étiquette viennoise, et s'enfonce vite dans une profonde
                    dépression.</p>
                  <p>Les premiers temps du mariage, le couple prend ses quartiers au château de Laxenbourg, aux environs de la capitale. Élisabeth se sent perdue et surveillée par sa belle-mère -
                    une femme intelligente mais dirigiste et traumatisée par la révolution de 1848 - et par son entourage. L'empereur, qui adore pourtant sa femme, est peu présent. La guerre
                    de Crimée vient d'être déclarée, opposant la France de <strong>Napoléon III</strong> et l'Angleterre <strong>Victorienne</strong> à la Russie du <strong>tsar Nicolas</strong>, précieux allié de l'Autriche pendant la révolution
                    de 1848 puisqu'il permit aux Habsbourg-Lorraine de conserver en leur possession le vaste royaume de Hongrie. Le jeune empereur est accaparé par les obligations de sa fonction
                    et doit se rendre tous les jours à Vienne, au palais de la <strong>Hofburg</strong>, ou au palais de Schönbrunn et n'en revient que très tard dans la soirée. Élisabeth, adolescente narcissique,
                    e sent abandonnée. De là proviennent ses premières répugnances pour la vie conjugale.</p>
                  <p>Beaucoup plus tard, elle confiera à sa fille Marie-Valérie « le mariage est une institution absurde. Enfant de 15 ans, j'ai été vendue… » Cependant, elle est rapidement enceinte
                    et donnera naissance à quatre enfants : <strong>Sophie</strong> (1855-1857), <strong>Gisèle</strong> (1856-1932), <strong>Rodolphe</strong> (1858-1889), <strong>Marie</strong> (1868-1924)</p>

